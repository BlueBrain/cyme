#ifndef CYME_UNROLL_EXP_HPP
#define CYME_UNROLL_EXP_HPP

#include "core/simd_vector/math/detail/remez.hpp"

namespace numeric{

    /**  
        \brief final wrapper for the exp
    */
    template<class T,memory::simd O, int N>
    inline vec_unroll<T,O,N> exp(const vec_unroll<T,O,N>& rhs){
        vec_unroll<T,O,N> nrv;
        nrv.r0 = exp(rhs.r0);
        nrv.r1 = exp(rhs.r1);
        nrv.r2 = exp(rhs.r2);
        nrv.r3 = exp(rhs.r3);
//        for(int i=0; i<N; ++i)
//          nrv.data[i] = exp(rhs.data[i]);
        return nrv;
    }
}
#endif

